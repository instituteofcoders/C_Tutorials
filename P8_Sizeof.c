//Use of sizeof()
#include<stdio.h>
void main()
{
    int a=10;
    float b=25.34;
    char c='A';
    double d=542879.21456;
    printf("Size of int=%d\n",sizeof(a));
    printf("Size of float=%d\n",sizeof(b));
    printf("Size of char=%d\n",sizeof(c));
    printf("Size of double=%d",sizeof(d));
    getch();

}
